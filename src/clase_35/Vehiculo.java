package clase_35;

public class Vehiculo {
	/**
	 * Marca del Vehiculo
	 */
	String marca;
	String modelo;
	int anio;
	int cantidadRuedas;
	
	
	public Vehiculo(String marca,String modelo,int anio,int cantidadRuedas) {
		this.marca = marca;
		this.modelo = modelo; 
		this.anio=anio;
		this.cantidadRuedas= cantidadRuedas;
	}

}
